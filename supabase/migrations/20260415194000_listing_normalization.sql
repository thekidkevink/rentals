create table if not exists public.property_types (
  id bigint generated by default as identity primary key,
  code text not null unique,
  label text not null unique
);

create table if not exists public.cities (
  id bigint generated by default as identity primary key,
  name text not null unique
);

create table if not exists public.areas (
  id bigint generated by default as identity primary key,
  city_id bigint not null references public.cities (id) on delete cascade,
  name text not null,
  unique (city_id, name)
);

create table if not exists public.utilities (
  id bigint generated by default as identity primary key,
  code text not null unique,
  label text not null unique
);

create table if not exists public.listing_contacts (
  id uuid primary key default gen_random_uuid(),
  user_id uuid unique references auth.users (id) on delete cascade,
  first_name text not null,
  last_name text not null default '',
  whatsapp_number text not null,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.listing_images (
  id uuid primary key default gen_random_uuid(),
  listing_id uuid not null references public.listings (id) on delete cascade,
  image_url text not null,
  sort_order integer not null default 0,
  created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.listing_utilities (
  listing_id uuid not null references public.listings (id) on delete cascade,
  utility_id bigint not null references public.utilities (id) on delete cascade,
  primary key (listing_id, utility_id)
);

