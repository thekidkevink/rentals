values
  ('water', 'Water'),
  ('electricity', 'Electricity'),
  ('wifi', 'WiFi')
on conflict (code) do update
set label = excluded.label;

alter table public.listings
add column if not exists property_type_id bigint references public.property_types (id),
add column if not exists city_id bigint references public.cities (id),
add column if not exists area_id bigint references public.areas (id),
add column if not exists contact_id uuid references public.listing_contacts (id);

insert into public.cities (name)
select distinct city
from public.listings
where city is not null
on conflict (name) do nothing;

insert into public.areas (city_id, name)
select distinct cities.id, listings.suburb
from public.listings
join public.cities cities on cities.name = listings.city
where listings.suburb is not null
on conflict (city_id, name) do nothing;

update public.listings listings
set property_type_id = property_types.id
from public.property_types property_types
where lower(property_types.label) = lower(listings.property_type)
  and listings.property_type_id is null;

update public.listings listings
set city_id = cities.id
from public.cities cities
where cities.name = listings.city
  and listings.city_id is null;

update public.listings listings
set area_id = areas.id
from public.areas areas
join public.cities cities on cities.id = areas.city_id
where cities.name = listings.city
  and areas.name = listings.suburb
  and listings.area_id is null;

insert into public.listing_contacts (user_id, first_name, last_name, whatsapp_number)
select distinct on (listings.user_id)
  listings.user_id,
  split_part(trim(listings.poster_name), ' ', 1),
  case
    when strpos(trim(listings.poster_name), ' ') > 0 then trim(substr(trim(listings.poster_name), strpos(trim(listings.poster_name), ' ') + 1))
    else ''
  end,
  listings.whatsapp_number
from public.listings listings
where listings.user_id is not null
  and listings.poster_name is not null
  and listings.whatsapp_number is not null
on conflict (user_id) do update
set
  first_name = excluded.first_name,
  last_name = excluded.last_name,
  whatsapp_number = excluded.whatsapp_number,
  updated_at = timezone('utc', now());

insert into public.listing_contacts (first_name, last_name, whatsapp_number)
select
  split_part(trim(listings.poster_name), ' ', 1),
  case
    when strpos(trim(listings.poster_name), ' ') > 0 then trim(substr(trim(listings.poster_name), strpos(trim(listings.poster_name), ' ') + 1))
    else ''
  end,
  listings.whatsapp_number
from public.listings listings
where listings.user_id is null
  and listings.poster_name is not null
  and listings.whatsapp_number is not null;

update public.listings listings
set contact_id = contacts.id
from public.listing_contacts contacts
where listings.user_id is not null
  and contacts.user_id = listings.user_id
  and listings.contact_id is null;

update public.listings listings
set contact_id = contacts.id
from public.listing_contacts contacts
where listings.user_id is null
  and contacts.user_id is null
  and contacts.whatsapp_number = listings.whatsapp_number
  and contacts.first_name = split_part(trim(listings.poster_name), ' ', 1)
  and contacts.last_name = case
    when strpos(trim(listings.poster_name), ' ') > 0 then trim(substr(trim(listings.poster_name), strpos(trim(listings.poster_name), ' ') + 1))
    else ''
  end
  and listings.contact_id is null;

insert into public.listing_images (listing_id, image_url, sort_order)
select listings.id, listings.image_url, 0
from public.listings listings
where listings.image_url is not null
  and not exists (
    select 1
    from public.listing_images images
    where images.listing_id = listings.id
  );

insert into public.listing_utilities (listing_id, utility_id)
select listings.id, utilities.id
from public.listings listings
cross join lateral unnest(coalesce(listings.utilities_included, '{}')) as utility_label(label)
join public.utilities utilities on lower(utilities.label) = lower(utility_label.label)
on conflict do nothing;

